package q2;

public class Par<F, S> {
    private F primeiroPar;
    private S segundoPar;

    public F getPrimeiroPar() {
        return primeiroPar;
    }

    public void setPrimeiroPar(F primeiroPar) {
        this.primeiroPar = primeiroPar;
    }

    public S getSegundoPar() {
        return segundoPar;
    }

    public void setSegundoPar(S segundoPar) {
        this.segundoPar = segundoPar;
    }
}
